namespace local_elisprogram\event;

defined('MOODLE_INTERNAL') || die();

/**
 * Event for when a program is not completed.
 */
class crlm_curriculum_course_associated extends \core\event\base {

    /**
     * Init method.
     */
    protected function init() {
        $this->data['crud'] = 'r';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Returns description of what happened.
     *
     * @return string
     */
    public function get_description() {
        return 'A program course was associated';
    }

    /**
     * Returns localised general event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('crlm_curriculum_course_associated', 'local_elisprogram');
    }

    /**
     * Does this event replace a legacy event?
     *
     * @return string legacy event name
     */
    static public function get_legacy_eventname() {
        return 'crlm_curriculum_course_associated';
    }

    /**
     * Returns relevant URL.
     *
     * @return \moodle_url
     */
    public function get_url() {
        return new \moodle_url('/local/elisprogram/index.php');
    }

    /**
     * Legacy event data if get_legacy_eventname() is not empty.
     *
     * @return stdClass
     */
    protected function get_legacy_eventdata() {
        global $CFG;
        require_once($CFG->dirroot.'/local/elisprogram/lib/data/curriculumcourse.class.php');
        $legacyeventdata = new \curriculumcourse($this->other);
        return $legacyeventdata;
    }
}
